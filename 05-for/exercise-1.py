# Оператор FOR

# функція range() - повертає послідовність чисел


# example 1
for i in range(5):
    print("Number: ", i)
else: #
    print('Кінець циклу')


# example range(1,2,3)
k_start = 90
k_end = 20
k_step = -3

for k in range(k_start, k_end, k_step):
    print("Number: ", k)
else: #
    print('Кінець циклу')

